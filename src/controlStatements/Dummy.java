package controlStatements;

public class Dummy {

	public static void main(String[] args) {
//						int (/or %) int = int
		System.out.println(5%2);
		System.out.println(2%5);
		
		System.out.println(5%10.5); // int/float=float
		System.out.println(5%2.5); // int/float=float
		System.out.println(5.5%2); // folat/int =float
		
		System.out.println("========================");
		
		System.out.println(5/2);
		System.out.println(2/5);
		
		System.out.println(5/10.5); // int/float=float
		System.out.println(5/2.5); // int/float=float
		System.out.println(5.5/2); // folat/int =float
	}

}
